/*

숙제1) 사용자 한테 N을 입력받아 그 수가 소수인지 판별하는 
프로그램을 참고자료 없이 코딩이 가능하도록 연습합니다.
*/
import java.util.*;
class  HW1
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
		System.out.println("n을 입력하세요");
		int n = sc.nextInt();
		
		int i = 1;
		int count = 0;
		while (i<=n)
		{
			if (n%i==0)
				count+=1;
			i++;
		}
		if (count!=2)
					System.out.println("소수가 아닙니다");
				else
					System.out.println("소수입니다");
	}
}
