class WhileDoWhile 
{
	public static void main(String[] args) 
	{
		
		int j =100;
		do
		{
			System.out.println("Hello World!");  //뒤에서 조건식이 나오기 때문에 조건을 만족하지 않아도 프린트 됨.
			j++;
		}	
		while (j<=3);
		

	System.out.println("------------------------------");
		int i=100;
		while(i<=3)
		{
			System.out.println("Hello World!");
			i++;
		}
	}
}
